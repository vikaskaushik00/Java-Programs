package primitive.challange;

public class Main {

    public static void main(String[] args) {
        byte first = 10;
        short second = 12;
        int third = 15;
        long last = 50000 + ((first + second + third) * 10);
        System.out.println("Number of byte is : " + first);
        System.out.println("Number of short is : " + second);
        System.out.println("Number of int is : " + third);
        System.out.println("Number of long is : " + last);

    }
}
